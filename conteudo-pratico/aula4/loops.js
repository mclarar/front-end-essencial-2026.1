//loop mais comum
//util quando sabemos numero de vezes que o loop deve ser executado
for (let i = 0; i<5 ; i++){
    console.log("numero", i);    
}


//while
//é mais usado quando você quer repetir enquanto uma condição for verdadeira
let j = 0;
while(j < 6){
    console.log("numero", j);
    j++
}